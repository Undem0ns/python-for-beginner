# ประเภทตัวอักษร
string = "hello python"
print(type(string))

# ตำแหน่งของตัวอักษร
print(string[6])

# ความยาวของตัวอักษร
print(len(string))

# ช่วงตัวอักษร
newSrting = string[6:]
print(newSrting)

# แปลงชนิดของข้อมูล จาก ตัวเลขเป็นตัวอักษร
